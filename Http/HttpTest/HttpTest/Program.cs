        static DateTime proxyListUpdateTime = new DateTime(0L);

        public static List<WebProxy> proxyList = new List<WebProxy>();

        static void Main(string[] args)
        {
            int stopTime = 0;

            foreach (string arg in args)
            {
                try {
                    if (arg.IndexOf('m') > 0)
                    {
                        stopTime = int.Parse(arg.Substring(0, arg.IndexOf('m')));
                    } else {
                        string[] indexies = arg.Split(',');

                        foreach (string index in indexies) {
                            try {
                                if (index.IndexOf('-')>0) {
                                    string []nums = index.Split('-');
                                    if (nums.Length>=2) {
                                        int start = int.Parse(nums[0]);
                                        int end = int.Parse(nums[1]);
                                        for (int num=start; num<=end; num++) {
                                            Voter.AddMessageToPost(num);
                                        }
                                    }
                                }
                                else 
                                {
                                    Voter.AddMessageToPost(int.Parse(index));
                                }
                            }
                            catch (Exception e)
                            {
                            }
                        }
                    }
                }
                catch (Exception e) 
                {
                }
            }

            if (stopTime > 0)
            {
                Console.WriteLine("Stop in " + stopTime + " minutes.");
            }
            else
            {
                Console.WriteLine("Infinite loop");
            }
            Voter.InitializeMessagesToPost();

            for (int i = 0; i < ProxyList.proxies.Length; i++)
            {
                proxyList.Add(new WebProxy(ProxyList.proxies[i]));
            }
            Console.WriteLine("Built-in proxy: " + proxyList.Count);
            loadProxyList();

            DateTime saveNow = DateTime.Now;

            for (int i = 0; i < 100; i++)
            {
                Voter voter = new Voter();
                new Thread(new ThreadStart(voter.Vote)).Start();
            }

            do {
                Thread.Sleep(60000);
                Voter.CollectCounts(DateTime.Now - saveNow);
                loadProxyList();
                stopTime --;
            } while (stopTime != 0);
            Environment.Exit(0);
        }

        static private List<WebProxy> loadProxyList()
        {
            List<WebProxy> newList = proxyList;
            FileInfo file = new FileInfo("proxy.txt");

            try
            {
                if (file.Exists && file.LastWriteTime > proxyListUpdateTime)
                {
                    System.IO.StreamReader reader = new System.IO.StreamReader(file.FullName);
                    string line = null;
                    newList = new List<WebProxy>();
                    while ((line = reader.ReadLine()) != null)
                    {
                        try
                        {
                            newList.Add(new WebProxy(line));
                        }
                        catch (Exception e)
                        {
                        }
                    }
                    if (newList.Count > 0)
                    {
                        proxyListUpdateTime = file.LastWriteTime;
                        proxyList = newList;
                        Console.WriteLine("Proxy list updated: " + newList.Count);
                    }
                    else
                    {
                        Console.WriteLine("Warn: Found empty proxy List");
                    }
                }
            }
            catch (Exception e)
            {
                Console.WriteLine("Warn: cannot access proxy.txt");
            }
            return proxyList;
        }
    }

