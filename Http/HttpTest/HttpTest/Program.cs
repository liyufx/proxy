        static string referer = "http://top.baidu.com/detail?ie=utf-8&b=16&w=%E5%A7%9A%E8%B4%9D%E5%A8%9C&c=9";
        static Random rnd = new Random();

        public void Vote()
        {
            WebProxy proxy = grabNewProxy();
            do
            {
                try
                {
                    proxy.BypassProxyOnLocal = true;

                    int index = messagesToPost[rnd.Next(0, messagesToPost.Count)];

                    HttpWebRequest request = (HttpWebRequest)WebRequest.Create(urls[index]);
                    request.Referer = referer;
                    request.UserAgent = "Mozilla/5.0";
                    // Set the Method property of the request to POST.
                    request.Method = "GET";
                    request.Proxy = proxy;

                    // Get the response.
                    WebResponse response = request.GetResponse();
                    // Display the status.
                    // Get the stream containing content returned by the server.
                    Stream dataStream = response.GetResponseStream();
                    // Open the stream using a StreamReader for easy access.
                    StreamReader reader = new StreamReader(dataStream);
                    // Read the content.
                    string responseFromServer = reader.ReadToEnd();
                    if (responseFromServer.IndexOf("\"success\":1") >= 0)
                    {
                        Interlocked.Increment(ref successCount);
                    }
                    else
                    {
                        Interlocked.Decrement(ref failureCount);
                        proxy = grabNewProxy();
                    }
                    // Display the content.
                    // Clean up the streams.
                    reader.Close();
                    dataStream.Close();
                    response.Close();
                }
                catch (Exception e)
                {
                    Interlocked.Increment(ref failureCount);
                    proxy = grabNewProxy();
                }
            } while (true);

        }

        internal static void CollectCounts(TimeSpan timespan)
        {
            totalSuccess += successCount;
            totalFailure += failureCount;
            Console.WriteLine("Voted=" + totalSuccess + "/" + successCount +
                    " (" + timespan + ")" + " Failure=" + totalFailure + "/" + failureCount);
            successCount = 0;
            failureCount = 0;
        }

        private static WebProxy grabNewProxy()
        {
            List<WebProxy> proxies = Program.proxyList;
            WebProxy proxy = proxies[rnd.Next(0, proxies.Count)];
            return proxy;
        }

        internal static void AddMessageToPost(int index)
        {
            if (index >= 1 && index <= urls.Length)
            {
                messagesToPost.Add(index-1);
            }
            else if (index == 0)
            {
                for (int i = 0; i < urls.Length; i++)
                {
                    messagesToPost.Add(i);
                }
            }
        }

        internal static void InitializeMessagesToPost()
        {
            if (messagesToPost.Count > 0)
            {
                Console.Write("Post messages: ");
                for (int i = 0; i < messagesToPost.Count; i++)
                {
                    Console.Write(messagesToPost[i]+1);
                    Console.Write(", ");
                }
                Console.WriteLine();
            }
            else
            {
                messagesToPost.Add(0);
                Console.WriteLine("Post message: 1");
            }
        }
